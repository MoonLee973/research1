@dataclass
class ModelResult:
    """
    모델의 최종 성능 지표 및 표준편차를 저장하는 데이터 클래스
    """
    name: str; counts: BinaryCounts;
    precision: float; recall: float; f2: float; ni: float;
    reject_rate_overall: float;
    precision_std: float; recall_std: float; f2_std: float; ni_std: float;
    reject_rate_overall_std: float;
    reject_rate_by_class: Tuple[float, float]; eq_cost_matrix: np.ndarray

class Aggregator:
    """
    여러 반복 실험 결과를 누적하고 요약하는 클래스
    """
    def __init__(self, model_names: List[str]):
        self._all_counts: Dict[str, List[BinaryCounts]] = {k: [] for k in model_names}
        self._eq_cost_accum: Dict[str, List[np.ndarray]] = {k: [] for k in model_names}

    def add(self, model_name: str, cnt: BinaryCounts, eq_cost: np.ndarray):
        self._all_counts[model_name].append(cnt)
        self._eq_cost_accum[model_name].append(eq_cost)

    def summarize(self) -> Dict[str, ModelResult]:
        out = {}
        for name, counts_list in self._all_counts.items():
            # 1. 각 반복(run)에 대한 개별 성능 지표 계산
            run_metrics = {
                'precision': [], 'recall': [], 'f2': [], 'ni': [], 'reject_rate': []
            }
            for cnt in counts_list:
                prec, rec, f2 = prf2_from_counts(cnt)
                run_metrics['precision'].append(prec)
                run_metrics['recall'].append(rec)
                run_metrics['f2'].append(f2)

                total = cnt.TN + cnt.FP + cnt.FN + cnt.TP + cnt.RN + cnt.RP
                if cnt.RN + cnt.RP > 0:
                    conf = cnt.to_matrix_with_reject()
                    rej_rate = (cnt.RN + cnt.RP) / total if total > 0 else 0.0
                    ni = ni_from_confusion(conf, m=2)
                else:
                    rej_rate = 0.0
                    ni = ni_from_confusion(cnt.to_matrix_no_reject(), m=2)
                run_metrics['reject_rate'].append(rej_rate)
                run_metrics['ni'].append(ni)

            # 2. 각 지표의 표준편차 계산
            prec_std = np.std(run_metrics['precision'])
            rec_std = np.std(run_metrics['recall'])
            f2_std = np.std(run_metrics['f2'])
            ni_std = np.std(run_metrics['ni'])
            rej_std = np.std(run_metrics['reject_rate'])

            # 3. 전체 반복에 대한 통합 성능 지표 계산 (기존 방식)
            total_counts = BinaryCounts(0,0,0,0,0,0)
            for cnt in counts_list:
                total_counts = total_counts.merge(cnt)

            agg_prec, agg_rec, agg_f2 = prf2_from_counts(total_counts)
            if total_counts.RN + total_counts.RP > 0:
                conf = total_counts.to_matrix_with_reject(); total = conf.sum()
                agg_rej_overall = (total_counts.RN + total_counts.RP) / total
                CN = conf[0, :].sum(); CP = conf[1, :].sum()
                rejN = conf[0, 2] / CN if CN > 0 else 0.0
                rejP = conf[1, 2] / CP if CP > 0 else 0.0
                agg_ni = ni_from_confusion(conf, m=2)
            else:
                agg_rej_overall, rejN, rejP = 0.0, 0.0, 0.0
                agg_ni = ni_from_confusion(total_counts.to_matrix_no_reject(), m=2)

            mats = self._eq_cost_accum[name]
            if mats:
                max_cols = max(mat.shape[1] for mat in mats)
                mats_norm = [np.hstack([mat, np.zeros((2, max_cols - mat.shape[1]))]) if mat.shape[1] < max_cols else mat for mat in mats]
                eq_avg = np.mean(np.stack(mats_norm, axis=0), axis=0)
            else: eq_avg = np.zeros((2, 2))

            # 4. 최종 결과 객체 생성
            out[name] = ModelResult(
                name=name, counts=total_counts,
                precision=agg_prec, recall=agg_rec, f2=agg_f2, ni=agg_ni,
                reject_rate_overall=agg_rej_overall,
                precision_std=prec_std, recall_std=rec_std, f2_std=f2_std,
                ni_std=ni_std, reject_rate_overall_std=rej_std,
                reject_rate_by_class=(rejN, rejP), eq_cost_matrix=eq_avg
            )
        return out

