def load_dataset(path: str, target: Optional[str] = None, id_col: Optional[str] = None) -> Tuple[np.ndarray, np.ndarray, List[str]]:
    """
    CSV 파일에서 데이터셋을 로드하고 전처리하는 함수
    """
    df = pd.read_csv(path)
    if id_col is not None and id_col in df.columns:
        df = df.drop(columns=[id_col])

    if target is None:
        for cand in ["target", "label", "y", "class", "Bankrupt?"]:
            if cand in df.columns:
                target = cand
                break
        if target is None:
            target = df.columns[-1]
    y = df[target].values
    X_df = df.drop(columns=[target])

    nunique = X_df.nunique(dropna=False)
    const_cols = nunique[nunique <= 1].index.tolist()
    if const_cols:
        print(f"상수 변수 제거: {const_cols}")
        X_df = X_df.drop(columns=const_cols)

    for col in X_df.columns:
        if not np.issubdtype(X_df[col].dtype, np.number):
            X_df[col] = pd.factorize(X_df[col], sort=True)[0]

    if y.dtype.kind not in ("i", "u", "f"):
        y_enc, _ = pd.factorize(y, sort=True)
        y = y_enc
    else:
        uniques = np.unique(y)
        mapper = {val: i for i, val in enumerate(sorted(uniques))}
        y = np.vectorize(mapper.get)(y)

    if len(np.unique(y)) != 2:
        raise ValueError("This script expects a binary target.")

    return X_df.values.astype(float), y.astype(int), X_df.columns.tolist()

