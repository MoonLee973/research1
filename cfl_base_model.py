def pred_nonabstain_with_alpha(p_pos: np.ndarray, alpha_N: float) -> np.ndarray:
    theta = alpha_N / (1.0 + alpha_N)
    return (p_pos >= theta).astype(int)

def pred_abstain_with_Tr(p_pos: np.ndarray, TrN: float, TrP: float) -> np.ndarray:
    p_neg = 1.0 - p_pos
    denomN = max(1e-12, 1.0 - TrN); denomP = max(1e-12, 1.0 - TrP)
    sN = p_neg / denomN; sP = p_pos / denomP
    y_pred = np.full_like(p_pos, fill_value=-1, dtype=int)
    choose_mask = (np.maximum(sN, sP) >= 1.0)
    choose_idx = np.where(choose_mask)[0]
    y_pred[choose_idx] = (sP[choose_idx] >= sN[choose_idx]).astype(int)
    return y_pred

def objective_alpha(alpha: float, p_pos: np.ndarray, y_true: np.ndarray) -> float:
    y_pred = pred_nonabstain_with_alpha(p_pos, alpha)
    return -ni_from_confusion(count_binary(y_true, y_pred).to_matrix_no_reject(), m=2)

def _uv_to_Tr(u: float, v: float, eps: float = 1e-6) -> Tuple[float, float]:
    a = 1.0 / (1.0 + math.exp(-u)); b = 1.0 / (1.0 + math.exp(-v))
    TrN = (1.0 - eps) * a; TrP = (1.0 - eps) * b * (1.0 - a)
    return TrN, TrP

def _Tr_to_uv(TrN: float, TrP: float, eps: float = 1e-6) -> Tuple[float, float]:
    a = TrN / (1.0 - eps)
    a = max(eps, min(1.0 - eps, a))
    u = math.log(a / (1.0 - a))
    if (1.0 - a) > eps:
        b = TrP / ((1.0 - eps) * (1.0 - a))
        b = max(eps, min(1.0 - eps, b))
    else:
        b = 0.5
    v = math.log(b / (1.0 - b))
    return u, v

def objective_uv_Tr(params: np.ndarray, p_pos: np.ndarray, y_true: np.ndarray, alpha_mean: float) -> float:
    u, v = float(params[0]), float(params[1])
    TrN, TrP = _uv_to_Tr(u, v)
    denom = 1.0 - TrN - TrP
    if denom <= 1e-9:
        return 1e6
    lam_FP = alpha_mean
    lam_RN = (TrN * (1.0 - TrP) - TrN * TrP * lam_FP) / denom
    lam_RP = (-TrN * TrP + (1.0 - TrN) * lam_FP * TrP) / denom
    if lam_RP > 1.0 or lam_RN > lam_FP or lam_RP < 0 or lam_RN < 0:
        return 1e6
    y_pred = pred_abstain_with_Tr(p_pos, TrN, TrP)
    ni = ni_from_confusion(count_binary(y_true, y_pred).to_matrix_with_reject(), m=2)
    return -ni

