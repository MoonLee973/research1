def run_experiment(X: np.ndarray, y: np.ndarray, n_repeats: int,
                   random_seed: int, solver: str, max_iter: int) -> Tuple[Dict[str, ModelResult], Dict[str, List[float]]]:
    """
    전체 실험 파이프라인을 실행하는 메인 함수.
    수정: 반복별 NI 점수를 수집하는 per_repeat 반환 추가.
    """
    model_names = ["STD_LOGIT", "WEIGHTED_LOGIT", "NI_NO_REJECT", "NI_REJECT"]
    agg = Aggregator(model_names)

    per_repeat = {name: [] for name in model_names}

    for rep in range(n_repeats):
        print(f"Repeat {rep + 1}/{n_repeats}")

        split_seed1 = random_seed + rep
        split_seed2 = random_seed + rep + n_repeats
        model_seed1 = random_seed + rep + (2 * n_repeats)
        model_seed2 = random_seed + rep + (3 * n_repeats)
        search_seed = random_seed + rep + (4 * n_repeats)

        X_tr_val, X_te, y_tr_val, y_te = train_test_split(
            X, y, test_size=0.2, random_state=split_seed1, stratify=y
        )
        X_tr, X_va, y_tr, y_va = train_test_split(
            X_tr_val, y_tr_val, test_size=0.25, random_state=split_seed2, stratify=y_tr_val
        )

        print(f"  Train: {len(y_tr)}, Validation: {len(y_va)}, Test: {len(y_te)}")

        scaler = MinMaxScaler()
        X_tr_s = scaler.fit_transform(X_tr)
        X_va_s = scaler.transform(X_va)
        X_te_s = scaler.transform(X_te)

        base_model = LogisticRegression(solver=solver, max_iter=max_iter, random_state=model_seed1)
        base_model.fit(X_tr_s, y_tr)

        p_va = base_model.predict_proba(X_va_s)[:, 1]
        p_te = base_model.predict_proba(X_te_s)[:, 1]

        # --- NI_NO_REJECT 모델 최적화 및 평가 ---
        res = minimize_scalar(objective_alpha, bounds=(0.001, 1.0), method='bounded', args=(p_va, y_va))
        alpha_mean = float(res.x)
        y_pred_ni_no_rej = pred_nonabstain_with_alpha(p_te, alpha_mean)
        cnt_ni_no = count_binary(y_te, y_pred_ni_no_rej)
        ni_no = ni_from_confusion(cnt_ni_no.to_matrix_no_reject(), m=2)
        lam_FP = alpha_mean
        agg.add("NI_NO_REJECT", cnt_ni_no, np.array([[0.0, lam_FP], [1.0, 0.0]]))
        per_repeat["NI_NO_REJECT"].append(float(ni_no))

        # --- NI_REJECT 모델 최적화 및 평가 ---
        search_rng = np.random.RandomState(search_seed)
        best_overall_ni = -1.0
        best_overall_Tr_pair = (0.0, 0.0)
        n_initial_points = 100

        for _ in range(n_initial_points):
            start_tr_n = search_rng.uniform(0, 0.95)
            start_tr_p = search_rng.uniform(0, 1.0 - start_tr_n)
            start_u, start_v = _Tr_to_uv(start_tr_n, start_tr_p)

            res_powell = minimize(
                objective_uv_Tr,
                np.array([start_u, start_v]),
                args=(p_va, y_va, alpha_mean),
                method='Powell',
                options={'maxiter': 30, 'xtol': 1e-2}
            )

            current_ni = -res_powell.fun
            if current_ni > best_overall_ni:
                best_overall_ni = current_ni
                best_overall_Tr_pair = _uv_to_Tr(float(res_powell.x[0]), float(res_powell.x[1]))

        TrN_opt, TrP_opt = best_overall_Tr_pair
        y_pred_ni_rej = pred_abstain_with_Tr(p_te, TrN_opt, TrP_opt)
        cnt_ni_rej = count_binary(y_te, y_pred_ni_rej)

        denom = max(1e-9, 1.0 - TrN_opt - TrP_opt)
        lam_RN = float(max(0.0, (TrN_opt * (1.0 - TrP_opt) - TrN_opt * TrP_opt * lam_FP) / denom))
        lam_RP = float(max(0.0, (-TrN_opt * TrP_opt + (1.0 - TrN_opt) * TrP_opt * lam_FP) / denom))
        eq_cost_rej = np.array([[0.0, lam_FP, lam_RN], [1.0, 0.0, lam_RP]])
        agg.add("NI_REJECT", cnt_ni_rej, eq_cost_rej)
        ni_rej = ni_from_confusion(cnt_ni_rej.to_matrix_with_reject(), m=2)
        per_repeat["NI_REJECT"].append(float(ni_rej))

        # --- Baselines (테스트 세트로 평가) ---
        # STD_LOGIT
        y_pred_std = (p_te >= 0.5).astype(int)
        cnt_std = count_binary(y_te, y_pred_std)
        agg.add("STD_LOGIT", cnt_std, np.array([[0.0, 1.0], [1.0, 0.0]]))
        ni_std = ni_from_confusion(cnt_std.to_matrix_no_reject(), m=2)
        per_repeat["STD_LOGIT"].append(float(ni_std))

        # WEIGHTED_LOGIT
        wt_model = LogisticRegression(solver=solver, max_iter=max_iter, random_state=model_seed2)
        wt_model.fit(X_tr_s, y_tr)
        p_te_wt = wt_model.predict_proba(X_te_s)[:, 1]


        N = len(y_tr)
        N1 = np.sum(y_tr)
        N0 = N - N1
        y_pred_wt = (p_te_wt >= N1/N).astype(int)
        cost_FN = N/N1
        cost_FP = N/N0
        eq_cost_wt = np.array([[0.0, cost_FP], [cost_FN, 0.0]])

        cnt_wt = count_binary(y_te, y_pred_wt)
        agg.add("WEIGHTED_LOGIT", cnt_wt, eq_cost_wt)
        ni_wt = ni_from_confusion(cnt_wt.to_matrix_no_reject(), m=2)
        per_repeat["WEIGHTED_LOGIT"].append(float(ni_wt))

    return agg.summarize(), per_repeat


